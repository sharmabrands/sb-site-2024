var teamMembers = document.getElementsByClassName("team-member");
var popupModalBg = document.getElementById("popup-modal-bg");
var modalContent = document.getElementById("modal-content");
var modalName = document.getElementById("modal-team-member-name");
var modalDesc = document.getElementById("modal-team-member-description");

for (let mem of teamMembers) {
  let member = mem.id;
  let memberName = mem.getElementsByClassName("name")[0].innerText;
  let memberPic = mem.getElementsByClassName("team-member-pic")[0];

  memberPic.addEventListener("click", () => {
    modalName.innerText = `About ${memberName}`;

    // Split the text into parts based on the squiggly lines (~~)
    let parts = memberQuotes[member].split("~~");
    let finalString = "";

    // Iterate through the parts and apply strikethrough if it's between squiggly lines
    for (let i = 0; i < parts.length; i++) {
      if (i % 2 === 0) {
        // Regular text
        finalString += parts[i];
      } else {
        // Text between squiggly lines
        finalString += `<s>${parts[i]}</s>`;
      }
    }

    modalDesc.innerText = finalString;

    popupModalBg.style.display = "block";
    modalContent.style.display = "block";

    popupModalBg.style.opacity = "0.5";
    modalContent.style.opacity = "1";

    function closeModal() {
      popupModalBg.style.opacity = "0";
      modalContent.style.opacity = "0";

      popupModalBg.style.display = "none";
      modalContent.style.display = "none";

      return;
    }

    var closeButton = document.getElementById("modal-close-button");

    window.addEventListener("keydown", function (e) {
      if (e.key === "Escape") {
        closeModal();
      }
    });

    popupModalBg.addEventListener("click", () => {
      closeModal();
    });

    closeButton.addEventListener("click", () => {
      closeModal();
    });
  });
}
